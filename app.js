let createError = require('http-errors');
let express = require('express');
let path = require('path');
let cookieParser = require('cookie-parser');
let logger = require('morgan');
let mongoose = require('mongoose');

// cria um objeto de conex√µes com o banco
process.connections = {};
process.connections.master = mongoose.createConnection('mongodb://localhost/iv2-makeiteasy', { useNewUrlParser: true });

let boardRouter = require('./api/v1/routes/BoardRoutes');

let app = express();

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/api/v1/board', boardRouter);

// catch 404 and forward to error handler
app.use(function (req, res, next) {
	next(createError(404));
});

// error handler
app.use(function (err, req, res, next) {
	// set locals, only providing error in development
	res.locals.message = err.message;
	res.locals.error = req.app.get('env') === 'development' ? err : {};

	// render the error page
	res.status(err.status || 500).json({ error: true, message: err.message, stack: err.stack });
});

module.exports = app;
